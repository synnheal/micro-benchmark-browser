import type { SuiteResult } from "@/types/run";
import { ALL_BENCHES } from "@/lib/engine/registry";
import { formatNumber, formatMs } from "@/lib/utils/bytes";

export function formatMdReport(run: SuiteResult): string {
  const date = new Date(run.timestamp).toLocaleString();
  const lines: string[] = [];

  lines.push(`# Micro Benchmark Report`);
  lines.push("");
  lines.push(`**Date:** ${date}`);
  lines.push(`**ID:** ${run.id}`);
  if (run.label) lines.push(`**Label:** ${run.label}`);
  lines.push("");

  // System Info
  lines.push("## System Info");
  lines.push("");
  lines.push(`| Property | Value |`);
  lines.push(`| --- | --- |`);
  lines.push(`| Platform | ${run.systemInfo.platform} |`);
  lines.push(`| Cores | ${run.systemInfo.cores} |`);
  if (run.systemInfo.memory) {
    lines.push(`| Device Memory | ${run.systemInfo.memory} GB |`);
  }
  lines.push(`| Screen | ${run.systemInfo.screenWidth}x${run.systemInfo.screenHeight} @${run.systemInfo.devicePixelRatio}x |`);
  lines.push(`| Language | ${run.systemInfo.language} |`);
  lines.push("");

  // Score
  if (run.score) {
    lines.push("## Score");
    lines.push("");
    lines.push(`**Total: ${run.score.total}/100** (confidence: ${run.score.confidence})`);
    lines.push("");
    lines.push(`| Category | Score | Weight |`);
    lines.push(`| --- | --- | --- |`);
    for (const [cat, data] of Object.entries(run.score.categories)) {
      lines.push(`| ${cat.toUpperCase()} | ${data.score} | ${(data.weight * 100).toFixed(0)}% |`);
    }
    lines.push("");
  }

  // Results
  lines.push("## Results");
  lines.push("");
  lines.push(`| Benchmark | Median | Mean | P95 | StdDev | Stability |`);
  lines.push(`| --- | --- | --- | --- | --- | --- |`);

  for (const result of run.results) {
    const bench = ALL_BENCHES.find((b) => b.id === result.benchId);
    const name = bench?.name ?? result.benchId;
    const fmt = result.unit === "ms" ? formatMs : formatNumber;
    lines.push(
      `| ${name} | ${fmt(result.stats.median)} ${result.unit} | ${fmt(result.stats.mean)} | ${fmt(result.stats.p95)} | ${fmt(result.stats.stddev)} | ${result.stability} |`
    );
  }

  lines.push("");
  lines.push("---");
  lines.push("*Generated by Micro Benchmark Browser*");

  return lines.join("\n");
}
